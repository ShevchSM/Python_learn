first = 10
second = 30

result = first + second     # сумма
print('+ ', result)
print(type(result))

result = first - second     # разница
print('- ', result)
print(type(result))

result = first * second     # умножение
print('* ', result)
print(type(result))

result = first / second     # деление
print('/ ', result)
print(type(result))

result = first ** second    # возведение в степень
print('** ', result)
print(type(result))

result = first // second    # целочисленное деление
print('// ', result)
print(type(result))

result = first % second     # остаток от деления
print('% ', result)
print(type(result))

